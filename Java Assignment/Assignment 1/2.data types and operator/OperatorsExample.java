public class OperatorsExample {
    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println("Sum: " + (a + b)); // Arithmetic
        System.out.println("Is a greater? " + (a > b)); // Relational
        System.out.println("Logical AND: " + (a > 5 && b < 10)); // Logical
    }
}